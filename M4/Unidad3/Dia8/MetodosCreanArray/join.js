/* .join() => Toma los elementos de un array y los une devolviendo un string concatenado por el parametro pasado. Retorna un nuevo STRING. */

const palabrasSeparadas = ["Hola", "como", "estan?", "Tanto", "tiempo."];

const nuevaFrase = palabrasSeparadas.join(" ");

console.log(nuevaFrase);

